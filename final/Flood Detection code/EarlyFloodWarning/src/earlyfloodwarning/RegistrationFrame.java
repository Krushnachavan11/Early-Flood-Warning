    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String name=jTextField1.getText();
        String email=jTextField2.getText();
        String mobile=jTextField3.getText();
        String username=jTextField4.getText();
      
        char[] pass=jPasswordField1.getPassword();
        String password=String.valueOf(pass);
        char[] conpas=jPasswordField2.getPassword();
        String conpass=String.valueOf(conpas);
        
        if(name.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Name Is Mandatory");
        }
        
        else if(!isValidEmail(email))
        {
            JOptionPane.showMessageDialog(null,"Email Id Invalid");
        }
        else if(!isValidMobile(mobile))
        {
            JOptionPane.showMessageDialog(null,"Mobile number is invalid");
        }
        else if(username.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"User Name Is Mandatory");
        }
        else if(password.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Password Is Mandatory");
        }
        else if(conpass.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Confirm Password is Mandatory");
        }
        else if(!password.equals(conpass))

        JOptionPane.showMessageDialog(null,"Password And Confirm Password Is Not Matched");
        else
        {
            Registration rst=new Registration();

            if(rst.doRegistration(name,email,mobile,username,password))
            {
                JOptionPane.showMessageDialog(null,"Register Successfully");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

